		private static void 内省性判定(C02系統値計算_結果 c02結果, C06関連因子判定_結果 c06結果, C07集合因子判定_結果 c07結果)
		{
			if (c02結果.標準点G_活動性 < c02結果.標準点R_のん気 &&
				c02結果.標準点G_活動性 < c02結果.標準点T_思考性)
			{
				if (0 == c02結果.標準点D_抑うつ性 &&
					3 > c02結果.標準点C_気分の変化 &&
					3 > c02結果.標準点I_劣等感 &&
					3 > c02結果.標準点N_神経質 &&
					3 > c02結果.標準点O_主観性 &&
					3 > c02結果.標準点Co_協調性)
				{
					c07結果.mstr内省性 += 衝動型;

					C06関連因子判定.傾向を追加(楽天的でのんき_無責任さから_遅刻_無届欠勤_ルール無視が多い, c06結果);
				}
				else
				{
					c07結果.mstr内省性 += ノーブレーキ型;

					C06関連因子判定.傾向を追加(行動力に対しブレーキが効かない, c06結果);
				}
			}
			else if (c02結果.標準点G_活動性 < c02結果.標準点R_のん気 &&
				c02結果.標準点G_活動性 > c02結果.標準点T_思考性)
			{
				c07結果.mstr内省性 += 果断型;

				if (2 >= (c02結果.標準点R_のん気 - c02結果.標準点T_思考性))
				{
					C06関連因子判定.傾向を追加(決断が早く身体は軽妙に動けるが_内面は熟慮的, c06結果);
				}
				else
				{
					C06関連因子判定.傾向を追加("果断だが無理をしている事が多い。", c06結果);
				}
			}
			else if (c02結果.標準点G_活動性 > c02結果.標準点R_のん気 &&
				c02結果.標準点G_活動性 < c02結果.標準点T_思考性)
			{
				if (2 >= (c02結果.標準点T_思考性 - c02結果.標準点R_のん気))
				{
					c07結果.mstr内省性 += 大勢順応型;

					C06関連因子判定.傾向を追加(行動面は慎重だが_心理面は熟慮的ではない, c06結果);
				}
				else 
				{
					c07結果.mstr内省性 += 優柔不断型;

					C06関連因子判定.傾向を追加("優柔不断な傾向がある。", c06結果);
				}
			}
			else if (c02結果.標準点G_活動性 > c02結果.標準点R_のん気 &&
				c02結果.標準点G_活動性 > c02結果.標準点T_思考性)
			{
				c07結果.mstr内省性 += 熟慮型;

				if (2 < (c02結果.標準点G_活動性 - 
					((c02結果.標準点R_のん気 + c02結果.標準点T_思考性) / 2)))
				{
					C06関連因子判定.傾向を追加(考え過ぎて決断力に欠ける人が多い, c06結果);
				}
				else
				{
					C06関連因子判定.傾向を追加(熟慮的_計画的で緻密な行動をとる_管理者に向いている事が多い, c06結果);
				}
			}
			else
				c07結果.mstr内省性 += 普通;
		}

		private static void 主導性判定(C02系統値計算_結果 c02結果, C06関連因子判定_結果 c06結果, C07集合因子判定_結果 c07結果)
