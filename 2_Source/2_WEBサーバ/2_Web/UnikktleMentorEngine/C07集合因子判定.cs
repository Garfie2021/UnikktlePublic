		private static void 活動性判定(C02系統値計算_結果 c02結果, C06関連因子判定_結果 c06結果, C07集合因子判定_結果 c07結果)
		{
			if (c02結果.標準点Ag_攻撃性 > c02結果.標準点G_活動性 &&
				3 <= c02結果.標準点G_活動性)
			{
				c07結果.mstr活動性 += "積極行動型";

				C06関連因子判定.傾向を追加("何事にも積極的、意欲的に取り組み、負けず嫌いな傾向がある。自尊心が強く、取扱の難しい人が多い。", c06結果);
			}
			else if (c02結果.標準点Ag_攻撃性 > c02結果.標準点G_活動性 &&
				2 >= c02結果.標準点G_活動性)
			{
				c07結果.mstr活動性 += "積極行動不一致型";

				C06関連因子判定.傾向を追加("意欲はあるが行動が伴わないタイプ。", c06結果);
			}
			else if (c02結果.標準点Ag_攻撃性 < c02結果.標準点G_活動性)
			{
				c07結果.mstr活動性 += "消極行動型";
	
				//C06関連因子判定.傾向を追加("性格は温順で自分から積極的に動こうとしないタイプ。");
				C06関連因子判定.傾向を追加("性格は温順だが行動力に富むタイプ。", c06結果);
			}
			else
				c07結果.mstr活動性 += "普通";

			if (2 <= (c02結果.標準点Ag_攻撃性 - c02結果.標準点G_活動性))
			{
				C06関連因子判定.傾向を追加(意欲と行動の不一致なタイプで_欲求不満が溜まりやすい, c06結果);
			}
			else if (-2 >= (c02結果.標準点Ag_攻撃性 - c02結果.標準点G_活動性))
			{
				C06関連因子判定.傾向を追加(生活態度が積極的なタイプ, c06結果);
			}
		}

