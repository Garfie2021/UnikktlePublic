		private static void 情緒安定性判定(C02系統値計算_結果 c02結果, C07集合因子判定_結果 c07結果)
		{
			if (C00Common.ChkDCINで突出がある(c02結果) == true)
				c07結果.mstr情緒安定性 += 不安定;
			else if (3 < c02結果.標準点D_抑うつ性 ||
				3 < c02結果.標準点C_気分の変化 ||
				3 < c02結果.標準点I_劣等感 ||
				3 < c02結果.標準点N_神経質)
				c07結果.mstr情緒安定性 += 不安定;
			else if (3 == c02結果.標準点D_抑うつ性 &&
				3 == c02結果.標準点C_気分の変化 &&
				3 == c02結果.標準点I_劣等感 &&
				3 == c02結果.標準点N_神経質)
				c07結果.mstr情緒安定性 += 普通;
			else
				c07結果.mstr情緒安定性 += 安定;
		}

