				return D型セールスマン(c02結果, c07結果);
			}

			return "";
		}

		private static string 労務職資質(Gender gender, C02系統値計算_結果 c02結果, C03系統判定_結果 c03系統結果)
		{
			if (gender == Gender.Male &&
				c03系統結果.mstr系統コード.IndexOf("E類") > -1)
				return "";

			if ((1 <= c02結果.標準点O_主観性 && c02結果.標準点O_主観性 <= 2) &&
				(1 <= c02結果.標準点Co_協調性 && c02結果.標準点Co_協調性 <= 2) &&
				(2 <= c02結果.標準点Ag_攻撃性 && c02結果.標準点Ag_攻撃性 <= 3) &&
				(c02結果.標準点G_活動性 > c02結果.標準点R_のん気) &&
				(c02結果.標準点G_活動性 > c02結果.標準点T_思考性))
			{
				return "○";
			}

			if (5 > c02結果.標準点O_主観性 &&
				5 > c02結果.標準点Co_協調性 &&
				5 > c02結果.標準点Ag_攻撃性)
			{
				return "△";
			}

			return "";
		}

		private static string サービス職資質(C02系統値計算_結果 c02結果, C03系統判定_結果 c03系統結果, C07集合因子判定_結果 c07結果)
		{
			if (c03系統結果.mstr系統コード.IndexOf("B類") > -1)
			{
				return B型サービス職(c02結果);
			}
			else if (c03系統結果.mstr系統コード.IndexOf("D類") > -1)
			{
				return D型サービス職(c02結果);
			}
			else
			{
				if (c07結果.mstr情緒安定性 != "不安定" &&
				    c07結果.mstr社会適応性 == "普通" &&
				    c07結果.mstr活動性 != "積極行動不一致型" &&
				    c07結果.mstr内省性 != "優柔不断型" &&
				    c07結果.mstr社交性 != "非社交的")
					return "○";
			}

			return "";
		}

		private static string B型サービス職(C02系統値計算_結果 c02結果)
		{
			if (4 >= c02結果.標準点D_抑うつ性 &&
				4 >= c02結果.標準点C_気分の変化 &&
				4 >= c02結果.標準点I_劣等感 &&
				4 >= c02結果.標準点N_神経質 &&
				4 >= c02結果.標準点O_主観性 &&
				4 >= c02結果.標準点Co_協調性 &&
				(3 <= c02結果.標準点Ag_攻撃性 && c02結果.標準点Ag_攻撃性 <= 4) &&
				(3 <= c02結果.標準点G_活動性 && c02結果.標準点G_活動性 <= 4) &&
				(4 <= c02結果.標準点A_支配性 && c02結果.標準点A_支配性 <= 5) &&
				(4 <= c02結果.標準点S_社会性 && c02結果.標準点S_社会性 <= 5))
			{
				if ((c02結果.標準点G_活動性 + 1) < c02結果.標準点R_のん気 &&
					(c02結果.標準点G_活動性 + 1) < c02結果.標準点T_思考性)
					return "△";
				else
					return "○";
			}

			return "";
		}

