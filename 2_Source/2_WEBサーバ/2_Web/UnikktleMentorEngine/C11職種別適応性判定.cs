		private static string A型セールスマン(C02系統値計算_結果 c02結果, C07集合因子判定_結果 c07結果)
		{
			//if (C00Common.ChkDCINで突出がある() == true)
			//    return "";
			if (0 <= c07結果.mstr情緒安定性.IndexOf("不安定"))
				return "";

			if (3 < c02結果.標準点Co_協調性)
				return "";

			if (3 != c02結果.標準点G_活動性)
				return "";

			if (4 > c02結果.標準点A_支配性)
				return "";

			if (4 > c02結果.標準点S_社会性)
				return "";

			if (1 == c02結果.標準点R_のん気 ||
				5 == c02結果.標準点R_のん気)
				return "";

			if (1 == c02結果.標準点T_思考性 ||
				5 == c02結果.標準点T_思考性)
				return "";

			if (c07結果.mstr共感性.IndexOf("高い") > -1)
				return "◎";
			else
				return "○";
		}

