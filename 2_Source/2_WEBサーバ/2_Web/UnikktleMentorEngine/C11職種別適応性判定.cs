		private static string 一般事務職資質(C02系統値計算_結果 c02結果)
		{
			if ((1 <= c02結果.標準点O_主観性 && c02結果.標準点O_主観性 <= 2) &&
				(1 <= c02結果.標準点Co_協調性 && c02結果.標準点Co_協調性 <= 2))
			{
				if ((2 <= c02結果.標準点D_抑うつ性 && c02結果.標準点D_抑うつ性 <= 3) &&
					(2 <= c02結果.標準点C_気分の変化 && c02結果.標準点C_気分の変化 <= 3) &&
					(2 <= c02結果.標準点I_劣等感 && c02結果.標準点I_劣等感 <= 3) &&
					(2 <= c02結果.標準点N_神経質 && c02結果.標準点N_神経質 <= 3))
				{
					if ((3 <= c02結果.標準点Ag_攻撃性 && c02結果.標準点Ag_攻撃性 <= 4) &&
						(3 <= c02結果.標準点G_活動性 && c02結果.標準点G_活動性 <= 4))
